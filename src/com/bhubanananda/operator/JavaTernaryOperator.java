package com.bhubanananda.operator;

/**
 * The Class JavaTernaryOperator.
 * 
 * @ author Bhubanananda
 */
public class JavaTernaryOperator {

	/**
	 * The main method.
	 *
	 * @param args the arguments
	 */
	public static void main(String[] args) {

		int a = 10;
		// condition ? if true : if false
		System.out.println(a == 10 ? a : 0);
	}
}
